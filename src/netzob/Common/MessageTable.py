class MessageTable(list):
    """MessageTable:

    This model represents a list of aligned messages.
    Initialy created to redefine the __str__ methods, this class
    can host specific methods"""

    def __repr__(self):

        cs = zip(*self)
        c_ws = [max(len(value) for value in c) for c in cs]
        format = ' | '.join(['%%%ds' % w for w in c_ws])
        result = [(format % tuple(r)) for r in self]
        return '\n'.join(result)
